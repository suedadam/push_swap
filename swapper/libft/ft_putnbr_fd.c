#include "libft.h"
#include <unistd.h>

void	ft_putnbr_fd(__int64_t n, int fd)
{
	if (n == -9223372036854775807 - 1)
	{
		ft_putstr_fd("-9223372036854775808", fd);
		return ;
	}
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n = -n;
	}
	if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putchar_fd(n % 10 + '0', fd);
	}
	if (n < 10)
		ft_putchar_fd(n % 10 + '0', fd);
}
